				     <p>O Linux é um núcleo monolítico: as funções do núcleo 
					 (escalonamento de processos, gerenciamento de memória, operações de entrada/saída, acesso ao sistema de arquivos) 
					 são executadas no espaço de núcleo. Uma característica do núcleo Linux é que algumas das funções 
					 (drivers de dispositivos, suporte à rede, sistema de arquivos, por exemplo) 
					 podem ser compiladas e executadas como módulos (em inglês: LKM - loadable kernel modules), 
					 que são bibliotecas compiladas separadamente da parte principal do núcleo e podem ser carregadas e descarregadas após o núcleo estar em execução.
					 
					 <br>
					 </br>			 
                     Embora Linus Torvalds não tivesse como objetivo inicial tornar o Linux um sistema portátil, 
					 ele evoluiu nessa direção. Linux é hoje um dos núcleos de sistemas operativos mais portáteis, 
					 correndo em sistemas desde o iPaq (um computador portátil) até o IBM S/390 (um denso e altamente custoso mainframe).


                     
                     <br>
					 </br>
                    
                     Os esforços de Linus foram também dirigidos a um diferente tipo de portabilidade. 
					 Portabilidade, de acordo com Linus, era a habilidade de facilmente compilar aplicações de uma variedade de código 
					 fonte no seu sistema; consequentemente, o Linux originalmente tornou-se popular em parte devido ao esforço para que 
					 os códigos-fonte GPL ou outros favoritos de todos corressem em Linux.
                    
                     
